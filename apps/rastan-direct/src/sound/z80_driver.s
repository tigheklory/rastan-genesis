    .section .rodata,"a"
    .global z80_driver_start
    .global z80_driver_end

/*
 * Z80 driver program bytes loaded by 68000 into Z80 RAM at 0x0000.
 * Mailbox: 0x1FF0 (unused in forced-tone bring-up mode)
 * Bring-up markers:
 *   0x1FF2 -> 0x5A when forced-tone loop entered
 *   0x1FF3 -> heartbeat incremented continuously
 */

z80_driver_start:
    /* 0000 */ .byte 0xF3                          /* di */
    /* 0001 */ .byte 0x31,0xF0,0x1F               /* ld sp,0x1FF0 */
    /* 0004 */ .byte 0xAF                          /* xor a */
    /* 0005 */ .byte 0x32,0xF0,0x1F               /* ld (0x1FF0),a */
    /* 0008 */ .byte 0x3E,0xFF                    /* ld a,0xFF */
    /* 000A */ .byte 0x32,0xF1,0x1F               /* ld (0x1FF1),a */
    /* 000D */ .byte 0xCD,0x4A,0x00               /* call 0x004A (set_patch) */
    /* 0010 */ .byte 0xCD,0xCE,0x00               /* call 0x00CE (key_off) */

    /* forced-tone loop (no mailbox dependency) */
    /* 0013 */ .byte 0xCD,0xD5,0x00               /* call 0x00D5 (tone_a) */
    /* 0016 */ .byte 0xCD,0xC7,0x00               /* call 0x00C7 (key_on) */
    /* 0019 */ .byte 0x3E,0x5A                    /* ld a,0x5A */
    /* 001B */ .byte 0x32,0xF2,0x1F               /* ld (0x1FF2),a: running marker */
    /* 001E */ .byte 0xAF                          /* xor a */
    /* 001F */ .byte 0x32,0xF3,0x1F               /* ld (0x1FF3),a: heartbeat */
    /* 0022 */ .byte 0xCD,0xC7,0x00               /* call 0x00C7 (key_on) */
    /* 0025 */ .byte 0x3A,0xF3,0x1F               /* ld a,(0x1FF3) */
    /* 0028 */ .byte 0x3C                          /* inc a */
    /* 0029 */ .byte 0x32,0xF3,0x1F               /* ld (0x1FF3),a */
    /* 002C */ .byte 0xC3,0x22,0x00               /* jp 0x0022 */
    /* 002F */ .byte 0x00,0x00,0x00,0x00,0x00,0x00,0x00
    /* 0036 */ .byte 0x00,0x00,0x00,0x00,0x00,0x00,0x00
    /* 003D */ .byte 0x00,0x00,0x00,0x00,0x00,0x00,0x00
    /* 0044 */ .byte 0x00,0x00,0x00,0x00,0x00,0x00

    /* 004A: set_patch */
    .byte 0x3E,0xB0,0x1E,0x07,0xCD,0xB7,0x00      /* B0=07 */
    .byte 0x3E,0xB4,0x1E,0xC0,0xCD,0xB7,0x00      /* B4=C0 */

    .byte 0x3E,0x30,0x1E,0x01,0xCD,0xB7,0x00      /* 30=01 */
    .byte 0x3E,0x34,0x1E,0x01,0xCD,0xB7,0x00      /* 34=01 */
    .byte 0x3E,0x38,0x1E,0x01,0xCD,0xB7,0x00      /* 38=01 */
    .byte 0x3E,0x3C,0x1E,0x01,0xCD,0xB7,0x00      /* 3C=01 */

    .byte 0x3E,0x40,0x1E,0x00,0xCD,0xB7,0x00      /* 40=00 */
    .byte 0x3E,0x44,0x1E,0x00,0xCD,0xB7,0x00      /* 44=00 */
    .byte 0x3E,0x48,0x1E,0x00,0xCD,0xB7,0x00      /* 48=00 */
    .byte 0x3E,0x4C,0x1E,0x00,0xCD,0xB7,0x00      /* 4C=00 */

    .byte 0x3E,0x50,0x1E,0x1F,0xCD,0xB7,0x00      /* 50=1F */
    .byte 0x3E,0x54,0x1E,0x1F,0xCD,0xB7,0x00      /* 54=1F */
    .byte 0x3E,0x58,0x1E,0x1F,0xCD,0xB7,0x00      /* 58=1F */
    .byte 0x3E,0x5C,0x1E,0x1F,0xCD,0xB7,0x00      /* 5C=1F */

    .byte 0x3E,0x80,0x1E,0x0F,0xCD,0xB7,0x00      /* 80=0F */
    .byte 0x3E,0x84,0x1E,0x0F,0xCD,0xB7,0x00      /* 84=0F */
    .byte 0x3E,0x88,0x1E,0x0F,0xCD,0xB7,0x00      /* 88=0F */
    .byte 0x3E,0x8C,0x1E,0x0F,0xCD,0xB7,0x00      /* 8C=0F */

    .byte 0xC9                                     /* ret */

    /* 00B7: ym_write(a=reg,e=data) */
    .byte 0x21,0x00,0x40                           /* ld hl,0x4000 */
    .byte 0x77                                     /* ld (hl),a */
    .byte 0x00                                     /* nop */
    .byte 0x00                                     /* nop */
    .byte 0x7B                                     /* ld a,e */
    .byte 0x23                                     /* inc hl */
    .byte 0x77                                     /* ld (hl),a */
    .byte 0x00                                     /* nop */
    .byte 0x00                                     /* nop */
    .byte 0x06,0x20                                /* ld b,0x20 */
    .byte 0x10,0xFE                                /* djnz -2 */
    .byte 0xC9                                     /* ret */

    /* 00C7: key_on */
    .byte 0x3E,0x28,0x1E,0xF0,0xCD,0xB7,0x00,0xC9

    /* 00CE: key_off */
    .byte 0x3E,0x28,0x1E,0x00,0xCD,0xB7,0x00,0xC9

    /* 00D5: tone_a */
    .byte 0x3E,0xA4,0x1E,0x22,0xCD,0xB7,0x00
    .byte 0x3E,0xA0,0x1E,0x69,0xCD,0xB7,0x00
    .byte 0xC9

    /* 00E2: tone_b */
    .byte 0x3E,0xA4,0x1E,0x26,0xCD,0xB7,0x00
    .byte 0x3E,0xA0,0x1E,0xC4,0xCD,0xB7,0x00
    .byte 0xC9

z80_driver_end:
